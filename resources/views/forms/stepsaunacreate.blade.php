<script>
document.addEventListener('DOMContentLoaded', function() {
    let currentStepDiy = 1;
    const totalStepsDiy = 5;
    
    const formDiy = document.getElementById('diy-form');
    const nextBtnDiy = document.getElementById('nextBtn-diy');
    const prevBtnDiy = document.getElementById('prevBtn-diy');
    const submitBtnDiy = document.getElementById('submitBtn-diy');
    
    // Step validation rules for DIY form
    const stepValidationDiy = {
        1: { 
            validate: function() {
                const width = formDiy.querySelector('input[name="SingleLine1"]');
                const height = formDiy.querySelector('input[name="SingleLine2"]');
                const depth = formDiy.querySelector('input[name="SingleLine"]');
                if (!width.value.trim() || !height.value.trim() || !depth.value.trim()) {
                    return 'Please enter all sauna dimensions';
                }
                return null;
            }
        },
        2: { 
            validate: function() {
                const heaterTypes = formDiy.querySelectorAll('input[name="Checkbox1"]:checked');
                const emfLevel = formDiy.querySelector('input[name="Radio5"]:checked');
                if (heaterTypes.length === 0) {
                    return 'Please select at least one heater type';
                }
                if (!emfLevel) {
                    return 'Please select an EMF level';
                }
                return null;
            }
        },
        3: { 
            validate: function() {
                const benchNumber = formDiy.querySelector('input[name="Radio"]:checked');
                const benchType = formDiy.querySelector('input[name="Radio1"]:checked');
                const benchTiers = formDiy.querySelector('input[name="Radio2"]:checked');
                if (!benchNumber) {
                    return 'Please select bench number';
                }
                if (!benchType) {
                    return 'Please select bench type';
                }
                if (!benchTiers) {
                    return 'Please select number of tiers';
                }
                return null;
            }
        },
        4: { 
            validate: function() {
                const doorPosition = formDiy.querySelector('input[name="Radio4"]:checked');
                if (!doorPosition) {
                    return 'Please select door position';
                }
                return null;
            }
        },
        5: { 
            validate: function() {
                const name = formDiy.querySelector('input[name="SingleLine3"]');
                if (!name.value.trim()) {
                    return 'Please enter your name';
                }
                return null;
            }
        }
    };
    
    // Show specific step
    function showStepDiy(step) {
        // Hide all steps
        document.querySelectorAll('#diy-form .step-content').forEach(content => {
            content.classList.remove('active');
            content.style.display = 'none';
        });
        
        // Show current step
        const currentStepElement = document.querySelector(`#diy-form .step-content[data-step="${step}"]`);
        if (currentStepElement) {
            currentStepElement.classList.add('active');
            currentStepElement.style.display = 'block';
        }
        
        // Update progress bar
        updateProgressBarDiy(step);
        
        // Update navigation buttons
        updateNavigationButtonsDiy(step);
        
        currentStepDiy = step;
    }
    
    // Update progress bar
    function updateProgressBarDiy(step) {
        document.querySelectorAll('#exampleModal .progress-step').forEach((stepElement, index) => {
            const stepNumber = index + 1;
            stepElement.classList.remove('active', 'completed');
            
            if (stepNumber === step) {
                stepElement.classList.add('active');
            } else if (stepNumber < step) {
                stepElement.classList.add('completed');
            }
        });
    }
    
    // Update navigation buttons
    function updateNavigationButtonsDiy(step) {
        prevBtnDiy.style.display = step === 1 ? 'none' : 'inline-block';
        
        if (step === totalStepsDiy) {
            nextBtnDiy.style.display = 'none';
            submitBtnDiy.style.display = 'inline-block';
        } else {
            nextBtnDiy.style.display = 'inline-block';
            submitBtnDiy.style.display = 'none';
        }
    }
    
    // Validate current step
    function validateStepDiy(step) {
        const validation = stepValidationDiy[step];
        if (validation && validation.validate) {
            const error = validation.validate();
            if (error) {
                alert(error);
                return false;
            }
        }
        return true;
    }
    
    // Change step function (scoped to DIY form)
    function changeStepDiy(direction) {
        const newStep = currentStepDiy + direction;
        
        if (direction > 0 && !validateStepDiy(currentStepDiy)) {
            return;
        }
        
        if (newStep >= 1 && newStep <= totalStepsDiy) {
            showStepDiy(newStep);
        }
    }
    
    // Add event listeners for DIY form buttons
    nextBtnDiy.addEventListener('click', function() {
        changeStepDiy(1);
    });
    
    prevBtnDiy.addEventListener('click', function() {
        changeStepDiy(-1);
    });
    
    // Initialize first step
    showStepDiy(1);
    
    // Form submission
    formDiy.addEventListener('submit', function(e) {
        if (!validateStepDiy(currentStepDiy)) {
            e.preventDefault();
        }
    });
});
</script>

