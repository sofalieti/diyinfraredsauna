document.addEventListener('DOMContentLoaded', function() {
    const faqItems = document.querySelectorAll('.faq-item');
    
    faqItems.forEach(item => {
        const question = item.querySelector('.faq-question');
        
        question.addEventListener('click', () => {
            const isActive = item.classList.contains('active');
            
            // Закрываем все остальные элементы
            faqItems.forEach(otherItem => {
                if (otherItem !== item) {
                    otherItem.classList.remove('active');
                }
            });
            
            // Переключаем текущий элемент
            item.classList.toggle('active', !isActive);
        });
    });

    // Добавляем обработчик для кнопки Contact Us
    const contactButton = document.querySelector('.faq-contact-block .btn-primary');
    if (contactButton) {
        contactButton.addEventListener('click', function(e) {
            e.preventDefault();
            const consultationModal = document.querySelector('#form_1');
            if (consultationModal) {
                const modal = new bootstrap.Modal(consultationModal);
                modal.show();
            }
        });
    }
});
</script>